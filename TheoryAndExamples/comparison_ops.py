# -----------------------------------------
#     ------ Comparison Operators ------
# -----------------------------------------
# [ == ] Equal
# [ != ] Not Equal
# [ > ] Greater Than
# [ < ] Less Than
# [ >= ] Greater than or Equal
# [ <= ] Less thank or Equal

# Examples
print(100 >= 100)
print(50 < 100)
print(60 != 60)
print(90 == 100)