####################################
# Arithmetic Operators
#-----------------------------------
# [+] Addition
# [-] subtraction
# [*] multiplication
# [/] Division
# [%] Modulus
# [**] Exponent 
# [//] Floor Division
#-------------------------------------
# Examples
print(15 % 2) # 1 so it is the remaining 
print(2 ** 5) # 32 2 * 2 * 2 * 2 * 2
print(110 // 20) # 5 the division without remaining 
print(120 // 20) # 6
print(130 // 20) # 6
